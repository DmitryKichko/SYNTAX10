package com.company.class3129;

public class Student {
    /*
    3. Create a Set collection that will hold Objects of Student Type. In this set we do
    not care about the insertion order. Each student object should have name and studentID.
    Display name of each student.

    3. 3. Создайте коллекцию Set, которая будет содержать объекты типа Student. В этом наборе нам
    не важен порядок вставки. Каждый объект студента должен иметь имя и studentID.
    Отобразим имя каждого студента.
     */

    String name;
    int studentID;

    Student(String name, int studentID) {
        this.name = name;
        this.studentID = studentID;
    }
}
