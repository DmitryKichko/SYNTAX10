package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int i=10;
		
		i=i+1;
		i+=1;
		
		System.out.println(i);
		i++;// i+1
		
		System.out.println(i);
		
		int a = 10;
		a-=1;
		
		a--; // a-1
		System.out.println(a);
		
		//10++; ++ или -- не работают с числами, применимы только для ПЕРЕМЕННЫХ
		
		
	}

}
